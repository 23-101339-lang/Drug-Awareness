function validateForm(e) {
    e.preventDefault();
    
    let isValid = true;
    

    document.querySelectorAll('.error').forEach(el => el.textContent = '');
    
    // Get values
    const name = document.getElementById('name').value.trim();
    const email = document.getElementById('email').value.trim();
    const message = document.getElementById('message').value.trim();
    

    if (name === '') {
        document.getElementById('nameError').textContent = 'Name is required';
        isValid = false;
    } else if (name.length < 3) {
        document.getElementById('nameError').textContent = 'Name must be at least 3 characters';
        isValid = false;
    }
    

    const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (email === '') {
        document.getElementById('emailError').textContent = 'Email is required';
        isValid = false;
    } else if (!emailPattern.test(email)) {
        document.getElementById('emailError').textContent = 'Please enter a valid email';
        isValid = false;
    }
    

    if (message === '') {
        document.getElementById('messageError').textContent = 'Message is required';
        isValid = false;
    } else if (message.length < 10) {
        document.getElementById('messageError').textContent = 'Message must be at least 10 characters';
        isValid = false;
    }
    
    if (isValid) {
        saveFormData({ name, email, message });
        showSuccessSendMsg();
        document.getElementById('contactForm').reset();
    }
    
    return false;
}

function saveFormData(data) {
    const messages = JSON.parse(localStorage.getItem('contactMessages') || '[]');
    messages.push({
        ...data,
        date: new Date().toLocaleString(),
        id: Date.now()
    });
    localStorage.setItem('contactMessages', JSON.stringify(messages));
}

function showSuccessSendMsg() {
    const msg = document.getElementById('successMsg');
    msg.classList.add('show');
    setTimeout(() => {
        msg.classList.remove('show');
    }, 3000);
}

function getSavedData() {
    const messages = JSON.parse(localStorage.getItem('contactMessages') || '[]');
    const table = document.getElementById('messagesTable');
    
    if (messages.length === 0) {
        table.innerHTML = '<p style="padding: 20px; text-align: center; color: var(--color-text-secondary);">No messages yet</p>';
        return;
    }
    
    let html = `
        <table>
            <thead>
                <tr>
                    <th>Name</th>
                    <th>Email</th>
                    <th>Message</th>
                    <th>Date</th>
                </tr>
            </thead>
            <tbody>
    `;
    
    messages.forEach(msg => {
        html += `
            <tr>
                <td>${msg.name}</td>
                <td>${msg.email}</td>
                <td>${msg.message.substring(0, 50)}${msg.message.length > 50 ? '...' : ''}</td>
                <td>${msg.date}</td>
            </tr>
        `;
    });
    
    html += '</tbody></table>';
    table.innerHTML = html;
}


